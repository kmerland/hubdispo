interface LanguageProviderProps {
  children: React.ReactNode;
}

export function LanguageProvider({ children }: LanguageProviderProps) {
  const [language, setLanguageState] = useState<Language>('fr');

  // Load language from localStorage on mount
  useEffect(() => {
    try {
      const savedLanguage = localStorage.getItem('hubdispo-language') as Language;
      if (savedLanguage && ['fr', 'en', 'nl'].includes(savedLanguage)) {
        setLanguageState(savedLanguage);
      } else {
        // Detect browser language
        const browserLang = navigator.language.toLowerCase();
        if (browserLang.startsWith('nl')) {
          setLanguageState('nl');
        } else if (browserLang.startsWith('en')) {
          setLanguageState('en');
        } else {
          setLanguageState('fr');
        }
      }
    } catch (error) {
      console.warn('Error loading language from localStorage:', error);
      setLanguageState('fr');
    }
  }, []);

  // Save language to localStorage when changed
  const setLanguage = (lang: Language) => {
    try {
      setLanguageState(lang);
      localStorage.setItem('hubdispo-language', lang);
      console.log('Language changed to:', lang); // Debug log
    } catch (error) {
      console.warn('Error saving language to localStorage:', error);
      setLanguageState(lang);
    }
  };

  // Translation function with parameter replacement
  const t = (key: string, params?: Record<string, string>) => {
    try {
      const langTranslations = translations[language];
      if (!langTranslations) {
        console.warn(`No translations found for language: ${language}`);
        return key;
      }
      
      const translation = langTranslations[key as keyof typeof translations['fr']] || key;
      
      if (params) {
        return Object.entries(params).reduce((acc, [paramKey, paramValue]) => {
          return acc.replace(new RegExp(`{${paramKey}}`, 'g'), paramValue);
        }, translation);
      }
      
      return translation;
    } catch (error) {
      console.warn('Error in translation function:', error);
      return key;
    }
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}